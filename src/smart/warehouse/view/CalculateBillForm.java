    private void btnTableAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTableAddActionPerformed
        WarehouseDTO warehouseDTO=storageBO.searchWarehouse(cmbWarehouseID.getSelectedItem().toString());
        Object[] data={warehouseDTO.getWarehouseCode(),warehouseDTO.getReseiveDate().toString(),warehouseDTO.getWidth(),warehouseDTO.getLength(),Double.parseDouble(lblWarehousePrice.getText())};
        ((DefaultTableModel)tblTerminatedStorages.getModel()).addRow(data);
        netTotal += Double.parseDouble(lblWarehousePrice.getText());
        lblTotalPrice.setText(String.valueOf(netTotal));
        cmbWarehouseID.removeItem(warehouseDTO.getWarehouseCode());
    }//GEN-LAST:event_btnTableAddActionPerformed

    private void cmbUserNicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbUserNicActionPerformed
        if(cmbUserNic.getSelectedItem()==null)return;
        
        try{
           UserDTO userDTO=userBO.searchUser(cmbUserNic.getSelectedItem().toString());
            int rowCount = tblTerminatedStorages.getRowCount();
           if(rowCount>0){
               tblTerminatedStorages.setModel(new DefaultTableModel(null,new String[]{"Warehouse id","Receive Date","Width","Length","Price"}));
            }
           lblUserName.setText(userDTO.getName());
           List<WarehouseDTO> warehouseDTOs=new ArrayList<>();
           warehouseDTOs.addAll(userDTO.getWarehouseDTOs());
           cmbWarehouseID.removeAllItems();
           reset();
           for (WarehouseDTO warehouseDTO : warehouseDTOs){
                cmbWarehouseID.addItem(warehouseDTO.getWarehouseCode());
           }  
       }catch(NotFoundException e){
           JOptionPane.showMessageDialog(this, e);
       }
        
    }//GEN-LAST:event_cmbUserNicActionPerformed

    private void cmbWarehouseIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbWarehouseIDActionPerformed
       if(cmbWarehouseID.getSelectedItem()==null)return;
       WarehouseDTO warehouseDTO=storageBO.searchWarehouse(cmbWarehouseID.getSelectedItem().toString());
       lblWarehouseLength.setText(Double.toString(warehouseDTO.getLength()) );
       lblWarehouseWidth.setText(Double.toString(warehouseDTO.getWidth()));
       lblWarehouseDes.setText(warehouseDTO.getDescription());
    
       double deference=Double.parseDouble(Long.toString(TimeUnit.DAYS.convert(Math.abs(java.sql.Date.valueOf(new SimpleDateFormat("yyyy-MM-dd").format(new Date())).getTime()-warehouseDTO.getReseiveDate().getTime()), TimeUnit.DAYS)));
  
       if(deference==0){
           lblWarehousePrice.setText(Double.toString(warehouseDTO.getLength()*warehouseDTO.getWidth()*1000));
        }else{
           lblWarehousePrice.setText(Double.toString(warehouseDTO.getLength()*warehouseDTO.getWidth()*1000*deference));
       }
    }//GEN-LAST:event_cmbWarehouseIDActionPerformed

    private void btnTableDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTableDeleteActionPerformed
        DefaultTableModel defaultTableModel=(DefaultTableModel) tblTerminatedStorages.getModel();
        if(tblTerminatedStorages.getSelectedRowCount()==1){
            int selectedRow = tblTerminatedStorages.getSelectedRow();
            netTotal -= Double.parseDouble(defaultTableModel.getValueAt(selectedRow, 4).toString());
            lblTotalPrice.setText(String.valueOf(netTotal));
            cmbWarehouseID.addItem(defaultTableModel.getValueAt(selectedRow, 0).toString());
            defaultTableModel.removeRow(tblTerminatedStorages.getSelectedRow());
            JOptionPane.showMessageDialog(rootPane, "Deleted!");
        }else if (tblTerminatedStorages.getRowCount()==0){
               JOptionPane.showMessageDialog(rootPane, "Table Is Empty!");
        }else{
              JOptionPane.showMessageDialog(rootPane, "Please Select one row");
        }
    }//GEN-LAST:event_btnTableDeleteActionPerformed

