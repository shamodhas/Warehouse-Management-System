    private void btnPayOnActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayOnActionActionPerformed
        List<ReturnDTO> returnDTOs=new ArrayList<>();
        int rowCount = tblTerminatedStorages.getRowCount();
        DefaultTableModel defaultTableModel=(DefaultTableModel) tblTerminatedStorages.getModel();
        java.sql.Date date=java.sql.Date.valueOf(lblDate.getText());
        for(int i=0; rowCount>i; i++){
           String warehuseCode=defaultTableModel.getValueAt(i, 0).toString();
           double price=Double.parseDouble(defaultTableModel.getValueAt(i, 4).toString());
           returnDTOs.add(new ReturnDTO(warehuseCode,date,price ));
        }
        if(storageBO.returnWarehouse(returnDTOs)){
            JOptionPane.showMessageDialog(this, "Payment Sucsuss!");
            CalculateBillForm calculateBillForm=new CalculateBillForm();
            calculateBillForm.setVisible(true);
            this.dispose();  
        }else{
            JOptionPane.showMessageDialog(this, "Payment NOT Sucsuss!");
            CalculateBillForm calculateBillForm=new CalculateBillForm();
            calculateBillForm.setVisible(true);
            this.dispose();  
        }
    }//GEN-LAST:event_btnPayOnActionActionPerformed

